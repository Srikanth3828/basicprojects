class  Monthnumber
{
	public static void main(String[] args) 
	{
		String month = "March";
		switch(month){
			case "January" : System.out.println(month + " is Winter");break;
			case "February" : System.out.println(month + " is Spring");break;
			case "March" : System.out.println(month + " is Spring");break;
			case "April" : System.out.println(month + " is Summer");break;
			case "May" : System.out.println(month + " is Summer");break;
			case "June" : System.out.println(month + " is Summer");break;
			case "July" : System.out.println(month + " is Monsoon");break;
			case "August" : System.out.println(month + "is Monsoon");break;
			case "September" : System.out.println(month + " is Monsoon");break;
			case "October" : System.out.println(month + " is Autumn");break;
			case "November" : System.out.println(month + " is Autumn");break;
			case "December" : System.out.println(month + " is Autumn");break;
			default : System.out.println("Invalid Month ");

		}
	}
}
