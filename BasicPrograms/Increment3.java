class Increment3
{
	public static void main(String[] args) 
	{
	int a = 20;
	System.out.println(a++);
	System.out.println(--a);
	System.out.println(++a);
	System.out.println(a);
	System.out.println(a+1);
	System.out.println(++a);
	System.out.println(a--);
	System.out.println(a++);
	System.out.println(--a);
	System.out.println(++a);

		}
}
