class  Vowelornot2
{
	public static void main(String[] args) 
	{
		char ch = 'o';
		switch(ch){
			case 'A' : 
			case 'a' : 
			case 'E' : 
			case 'e' : 
			case 'I' : 
			case 'i' : 
			case 'O' : 
			case 'o' : 
			case 'U' : 
			case 'u' : System.out.println(ch+ " is vowel");break;
			default : System.out.println(ch+ " is consonant");

		}
	}
}
